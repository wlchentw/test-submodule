static int rt9114_regmap_write_device(void *client, u32 addr,
				       int len, const void *src)
{
	return i2c_smbus_write_i2c_block_data(client, addr, len, src);
}

static struct rt_regmap_fops rt9114_regmap_fops = {
	.read_device = rt9114_regmap_read_device,
	.write_device = rt9114_regmap_write_device,
};

int rt9114_regmap_register(struct rt9114_info *info)
{
	rt9114_regmap_props.name = kasprintf(GFP_KERNEL,
					     "rt9114.%s", dev_name(info->dev));
	info->regmap = rt_regmap_device_register(&rt9114_regmap_props,
						 &rt9114_regmap_fops,
						 info->dev, info->i2c, info);
	return info->regmap ? 0 : -EINVAL;
}
EXPORT_SYMBOL_GPL(rt9114_regmap_register);

void rt9114_regmap_unregister(struct rt9114_info *info)
{
	rt_regmap_device_unregister(info->regmap);
}
