/* tokenize according to the new line
* restore whole line to the argv[i]
* return:
* number of lines
*/
static int tokenize_line(char *cmd, char *argv[], int len)
{
	char *pos;
	char *start;
	int argc = 0;

	start = pos = cmd;
	for (;;) {
		argv[argc] = pos;
		argc++;
		while (*pos != '\n') {
			pos++;
			if (pos - start >= len)
				break;
		}
		if (*pos == '\n') {
			*pos++ = '\0';
			if (pos - start >= len)
				break;
		}
	}

	return argc;
}

